import math

def розрахувати_плитку():
    # Введення розмірів площі
    довжина_площі = float(input("Введіть довжину площі (в метрах): "))
    ширина_площі = float(input("Введіть ширину площі (в метрах): "))

    # Введення розмірів плитки
    довжина_плитки = float(input("Введіть дожину плитки (в метрах): "))
    ширина_плитки = float(input("Введіть ширину плитки (в метрах): "))

    # Введення ширини шва
    ширина_шва = float(input("Введіть ширину шва (в метрах): "))

    # Обчислення коригованих розмірів плитки з урахуванням шва
    ефективна_довжина_плитки = довжина_плитки + ширина_шва
    ефективна_ширина_плитки = ширина_плитки + ширина_шва

    # Розрахунок кількості плиток по довжині та ширині
    кількість_по_довжині = math.ceil(довжина_площі / ефективна_довжина_плитки)
    кількість_по_ширині = math.ceil(ширина_площі / ефективна_ширина_плитки)

    # Загальна кількість плиток
    загальна_кількість = кількість_по_довжині * кількість_по_ширині

    # Додавання 10% запасу
    кількість_із_запасом = math.ceil(загальна_кількість * 1.1)

    print(f"\nПотрібно приблизно {кількість_із_запасом} плиток (із урахуванням шва та запасу 10%).")

розрахувати_плитку()

